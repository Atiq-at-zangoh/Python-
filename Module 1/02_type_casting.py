
fruits = ["Apple", "Banana", "Orange"] # List
list01 = ["Alexa", 45, True, 90.098]

# List to Set

print("Before Type casting: ",type(fruits))
fruits = set(fruits)
print("After Type casting",type(fruits))

# List to Tuple

print("Before Type casting: ",type(list01))
list01 = tuple(list01)
print("After Type casting",type(list01))





